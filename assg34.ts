// Define the array of pizza names
let pizzas: string[] = ["Pepperoni", "Tikka", "Fajita"];

// Print the names of each pizza using a for loop
console.log("Pizza names:");
for (let pizza of pizzas) {
    console.log(pizza);
}

// Print a sentence for each pizza
console.log("\nSentences about each pizza:");
for (let pizza of pizzas) {
    console.log(`I like ${pizza} pizza.`);
}

// Additional sentence expressing love for pizza
console.log("\nI really love pizza!");
for (let pizza of pizzas) {
    console.log(`I really love ${pizza} pizza`);

}
