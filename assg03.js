var personNames = "syed farhan hassan";
// Printing the person's name in Lowercase
console.log("Lowercase:", personNames.toLowerCase());
// Printing the person's name in uppercase
console.log("Uppercase:", personNames.toUpperCase());
// Printing the person's name in titlecase
console.log("Titlecase:", personNames.replace(/\b\w/g, function (char) { return char.toUpperCase(); }));
